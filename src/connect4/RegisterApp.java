    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        Student newStudent;
        
        //Storing user input
        String strFirst = this.txtFirst.getText();
        String strLast = this.txtLast.getText();
        String strAge = this.txtAge.getText();
        String strFull = strFirst + " " + strLast;
        //Creating new object in Student class
        newStudent = new Student(strFull, strStudentGrade, strAge);
             
        
        
        //Only modifies txt file if a new student is registering 
        if (LinearSearch(strFull, 0) == 1){
        System.out.println(ArrStudentInfo.size());
        ArrStudentInfo.add(newStudent);
        System.out.println(ArrStudentInfo.size());
        
        try
        {
            FileWriter WriteF = new FileWriter("src/connect4/StudentInfo.txt", true);           
            PrintWriter PrintF = new PrintWriter(WriteF);
            
            //Will print all the student information stored in the ArrayList into the StudentInfo.txt file
            //After running the program and registering, you can open the txt file to see that it has been modified
            
            for (int i = 0; i < ArrStudentInfo.size (); i++){
                PrintF.println (ArrStudentInfo.get(i).strName); 
                PrintF.println ("Age: " + ArrStudentInfo.get(i).strAge);
                PrintF.println(ArrStudentInfo.get(i).strGrade);
                PrintF.println();
            }                               
            
                PrintF.close();
        }
        catch (IOException e){}
               
        }
        
        else if (LinearSearch(strFull, 0) == 0){
            
            JOptionPane.showMessageDialog (null, "This student is already registered.", "Student Already Exists", JOptionPane.INFORMATION_MESSAGE);                     
        }    
                
    }//GEN-LAST:event_btnRegisterActionPerformed

