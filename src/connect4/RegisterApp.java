    //Method which checks to see if student name is already registered into the ArrayList
    /**
     * 
     * @param strFind as the name of the student registering 
     * @param intArrayIndex as the index of the element being compared to strFind
     * @return 
     */
    //**This method is done recursively**
    
    public int LinearSearch(String strFind, int intArrayIndex){
               
        if (intArrayIndex > ArrStudentInfo.size()-1){
            return 1; //Meaning not found
            
        }
        System.out.println(ArrStudentInfo.get(intArrayIndex).strName + " " + strFind);
        
        if (ArrStudentInfo.get(intArrayIndex).strName.equals(strFind)){//Found that student's name is already registered
            return 0;
            
        }
        else return LinearSearch(strFind, intArrayIndex + 1);
        
    }
    
    /**
     * This method helps store user's grade in sentence form
     * @param intGrade as the grade selected by the user
     * @return a String indicating their grade in sentence form
     */
    public String GradeOutput(int intGrade){
        switch(intGrade){
            case 1: return "This student is in Grade 1.";
 
            case 2: return "This student is in Grade 2.";
            
            case 3: return "This student is in Grade 3.";
            
            case 4: return "This student is in Grade 4.";
            
            case 5: return "This student is in Grade 5.";
        }
        return "N/A";
    }
